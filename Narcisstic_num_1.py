def narcissistic( value ):
    # int => modulo % 10 , divide 10 => NUMBER[Digit_0, Digit_1, ...]
    num = value
    NUMBER = []
    while num > 0:
        modulo = num % 10
        NUMBER.insert(0, modulo)
        num = int(num / 10)
        
    #  for each Digit in NUMBER[] : Digit ** len(NUMBER[])
    j = 0
    sum = 0
    for digit in NUMBER:
        NUMBER[j] = digit ** len(NUMBER)
        sum += NUMBER[j]
    if sum == value:
        return True
    else:
        return False
